document.addEventListener('DOMContentLoaded', () => {
    const categoryFilter = document.getElementById('categoryFilter');
    const productsGrid = document.getElementById('productsGrid');

    if (!categoryFilter || !productsGrid) return;

    // Render Custom Category Dropdown
    categoryFilter.innerHTML = '';

    const customSelect = document.createElement('div');
    customSelect.className = 'custom-select';
    customSelect.id = 'categorySelect';

    const trigger = document.createElement('div');
    trigger.className = 'custom-select-trigger';
    const triggerText = document.createElement('span');
    triggerText.textContent = 'All Products';
    const triggerIcon = document.createElement('i');
    triggerIcon.className = 'fa-solid fa-chevron-down';
    trigger.appendChild(triggerText);
    trigger.appendChild(triggerIcon);

    const optionsContainer = document.createElement('div');
    optionsContainer.className = 'custom-select-options';

    // Value property to mimic a select element
    customSelect.value = 'all';

    // Options Data
    const optionsData = [{ value: 'all', label: 'All Products' }];
    productData.forEach(catObj => {
        optionsData.push({ value: catObj.category, label: catObj.category });
    });

    const optionElements = [];

    optionsData.forEach(optData => {
        const optEl = document.createElement('div');
        optEl.className = 'custom-select-option';
        if (optData.value === 'all') optEl.classList.add('selected');
        optEl.dataset.value = optData.value;
        optEl.textContent = optData.label;

        optEl.addEventListener('click', () => {
            // Update Selection
            optionElements.forEach(el => el.classList.remove('selected'));
            optEl.classList.add('selected');
            triggerText.textContent = optData.label;
            customSelect.value = optData.value;
            customSelect.classList.remove('open');

            // Dispatch change event manually
            customSelect.dispatchEvent(new Event('change'));
        });

        optionElements.push(optEl);
        optionsContainer.appendChild(optEl);
    });

    customSelect.appendChild(trigger);
    customSelect.appendChild(optionsContainer);
    categoryFilter.appendChild(customSelect);

    // Toggle Dropdown
    trigger.addEventListener('click', (e) => {
        e.stopPropagation();
        customSelect.classList.toggle('open');
    });

    // Close when clicking outside
    document.addEventListener('click', (e) => {
        if (!customSelect.contains(e.target)) {
            customSelect.classList.remove('open');
        }
    });

    // Event listener for change
    customSelect.addEventListener('change', () => {
        const selectedVal = customSelect.value;
        if (selectedVal === 'all') {
            renderProducts(null);
        } else {
            renderProducts(selectedVal);
        }
    });

    function generateItemLink(item, catObj) {
        if (typeof item !== 'string' && item.link) {
            return item.link;
        }
        const itemName = typeof item === 'string' ? item : item.name;
        const formatStr = (str) => str.toLowerCase().replace(/\s*&\s*/g, '-and-').replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '');
        return `${formatStr(itemName)}-${formatStr(catObj.category)}`;
    }

    function renderProducts(categoryName) {
        productsGrid.innerHTML = '';

        let itemsToRender = [];

        if (categoryName === null) {
            // Render all items
            if (window.innerWidth <= 1250) {
                // Mobile view: list categories as stripes
                productData.forEach(catObj => {
                    itemsToRender.push({
                        name: catObj.category,
                        image: catObj.image,
                        isCategoryStripe: true,
                        targetCategory: catObj.category
                    });
                });
            } else {
                // Desktop view: previews
                productData.forEach(catObj => {
                    const previewItems = catObj.items.slice(0, 3);
                    previewItems.forEach(item => {
                        const itemName = typeof item === 'string' ? item : item.name;
                        const itemImage = typeof item === 'string' ? catObj.image : (item.image || catObj.image);
                        const itemLink = generateItemLink(item, catObj);
                        itemsToRender.push({ name: itemName, image: itemImage, link: itemLink, isViewMore: false });
                    });

                    if (catObj.items.length > 3) {
                        itemsToRender.push({
                            name: `${catObj.category}`,
                            image: catObj.image,
                            isViewMore: true,
                            targetCategory: catObj.category
                        });
                    }
                });
            }
        } else {
            // Render specific category items
            const catObj = productData.find(c => c.category === categoryName);
            if (catObj) {
                catObj.items.forEach(item => {
                    const itemName = typeof item === 'string' ? item : item.name;
                    const itemImage = typeof item === 'string' ? catObj.image : (item.image || catObj.image);
                    const itemLink = generateItemLink(item, catObj);
                    itemsToRender.push({ name: itemName, image: itemImage, link: itemLink, isViewMore: false });
                });
            }
        }

        itemsToRender.forEach((prod, index) => {
            const a = document.createElement('a');

            if (prod.isViewMore || prod.isCategoryStripe) {
                a.href = '#categoryFilter';
                a.addEventListener('click', (e) => {
                    e.preventDefault();
                    const selectEl = document.getElementById('categorySelect');
                    if (selectEl) {
                        const optionToSelect = selectEl.querySelector(`.custom-select-option[data-value="${prod.targetCategory}"]`);
                        if (optionToSelect) {
                            optionToSelect.click();
                        }
                        document.getElementById('categoryFilter').scrollIntoView({ behavior: 'smooth' });
                    }
                });
            } else {
                a.href = prod.link || '/';
            }

            a.className = 'prod prod--unified reveal active';
            a.style.transitionDelay = `${(index % 10) * 0.05}s`;

            const imgDiv = document.createElement('div');
            imgDiv.className = prod.isViewMore ? 'prod__img prod__img--unified prod__img--view-more' : 'prod__img prod__img--unified';
            imgDiv.setAttribute('role', 'img');
            imgDiv.setAttribute('aria-label', prod.name);
            imgDiv.setAttribute('alt', prod.name);

            if (!prod.isViewMore) {
                imgDiv.setAttribute('data-bg', prod.image);
                imgDiv.classList.add('lazy-bg');
            }

            // Container for the overlaid content
            const overlayContent = document.createElement('div');
            overlayContent.className = prod.isViewMore ? 'prod__overlay prod__overlay--view-more' : 'prod__overlay';

            const h5 = document.createElement('h5');
            h5.textContent = prod.name;

            if (prod.isViewMore) {
                const span = document.createElement('span');
                span.className = 'prod__link prod__link--view-more';
                span.innerHTML = 'View Full Category <i class="fa-solid fa-arrow-right"></i>';

                overlayContent.appendChild(h5);
                overlayContent.appendChild(span);

            } else {
                const span = document.createElement('span');
                span.className = 'prod__link';
                span.innerHTML = 'View Details <i class="fa-solid fa-arrow-right"></i>';

                overlayContent.appendChild(h5);
                overlayContent.appendChild(span);
            }

            imgDiv.appendChild(overlayContent);
            a.appendChild(imgDiv);

            productsGrid.appendChild(a);

            // Trigger the reveal animation slightly after appending to DOM
            setTimeout(() => {
                a.classList.add('is-in');
            }, 50);
        });

        // Initialize IntersectionObserver for lazy background images
        const lazyBgs = productsGrid.querySelectorAll('.lazy-bg');
        if ('IntersectionObserver' in window) {
            const bgObserver = new IntersectionObserver((entries, observer) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        const el = entry.target;
                        const bg = el.getAttribute('data-bg');
                        if (bg) {
                            el.style.backgroundImage = `url('${bg}')`;
                        }
                        el.classList.remove('lazy-bg');
                        observer.unobserve(el);
                    }
                });
            }, { rootMargin: '200px 0px' });

            lazyBgs.forEach(el => bgObserver.observe(el));
        } else {
            lazyBgs.forEach(el => {
                el.style.backgroundImage = `url('${el.getAttribute('data-bg')}')`;
                el.classList.remove('lazy-bg');
            });
        }
    }

    // Function to handle category selection based on URL hash
    function handleHashChange() {
        const hashCategory = window.location.hash.replace('#', '').toLowerCase();

        if (hashCategory) {
            // Attempt to match the hash to a category name using the same slug format
            const formatStr = (str) => str.toLowerCase().replace(/\s*&\s*/g, '-and-').replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '');
            const matchedCategory = productData.find(c => formatStr(c.category) === hashCategory);

            if (matchedCategory) {
                const optionToSelect = document.querySelector(`.custom-select-option[data-value="${matchedCategory.category}"]`);
                if (optionToSelect) {
                    optionToSelect.click();
                } else {
                    renderProducts(null); // Fallback to all
                }
            } else {
                renderProducts(null); // Fallback to all
            }
        } else {
            // Initial render (All Products)
            renderProducts(null);
        }
    }

    // Listen for hash changes so footer links work even while already on products.php
    window.addEventListener('hashchange', handleHashChange);

    // Initial check on page load
    handleHashChange();
});